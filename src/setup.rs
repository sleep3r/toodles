fn step_header(step: u8, total: u8, title: &str) {
    println!();
    println!(
        "  {} {}",
        style(format!("[{step}/{total}]")).cyan().bold(),
        style(title).bold(),
    );
    println!();
}

fn ask_telegram_token() -> Result<String> {
    step_header(1, 7, "Telegram Bot Token");
    println!(
        "  {}",
        style("Get your bot token from @BotFather on Telegram.").dim()
    );
    println!("  {}", style("Format: 123456789:ABCDef...").dim());
    println!();

    let token: String = Password::new()
        .with_prompt("  Bot token")
        .validate_with(|input: &String| -> Result<(), &str> {
            let parts: Vec<&str> = input.split(':').collect();
            if parts.len() == 2
                && parts[0].chars().all(|c| c.is_ascii_digit())
                && !parts[1].is_empty()
            {
                Ok(())
            } else {
                Err("Invalid token format. Expected: <number>:<alphanumeric>")
            }
        })
        .interact()
        .context("Failed to read bot token")?;

    println!("  {} Token saved.", style("✔").green().bold());
    Ok(token)
}

fn ask_allowed_users() -> Result<Option<String>> {
    step_header(2, 7, "User Access Control");
    println!(
        "  {}",
        style("Restrict who can use the bot by specifying Telegram user IDs.").dim()
    );
    println!(
        "  {}",
        style("Leaving this empty allows everyone to use the bot.").dim()
    );
    println!();

    let restrict = Confirm::new()
        .with_prompt("  Restrict access to specific users?")
        .default(false)
        .interact()
        .context("Failed to read confirmation")?;

    if !restrict {
        println!(
            "  {} All users will have access.",
            style("✔").green().bold()
        );
        return Ok(None);
    }

    let ids: String = Input::new()
        .with_prompt("  Allowed user IDs (comma-separated)")
        .validate_with(|input: &String| -> Result<(), &str> {
            let valid = input
                .split(',')
                .all(|s| !s.trim().is_empty() && s.trim().chars().all(|c| c.is_ascii_digit()));
            if valid {
                Ok(())
            } else {
                Err("Enter comma-separated numeric IDs, e.g. 123456789,987654321")
            }
        })
        .interact_text()
        .context("Failed to read user IDs")?;

    let cleaned: String = ids
        .split(',')
        .map(|s| s.trim())
        .collect::<Vec<_>>()
        .join(",");

    println!("  {} Allowed: {cleaned}", style("✔").green().bold());
    Ok(Some(cleaned))
}

fn ask_gemini_cli_path() -> Result<String> {
    step_header(3, 7, "Gemini CLI Path");

    // Try to detect if `gemini` is on PATH
    let found_on_path = std::process::Command::new("which")
        .arg("gemini")
        .output()
        .map(|o| o.status.success())
        .unwrap_or(false);

    if found_on_path {
        println!(
            "  {} {}",
            style("✔").green().bold(),
            style("gemini found on PATH.").dim()
        );
    } else {
        println!(
            "  {} {}",
            style("⚠").yellow().bold(),
            style("gemini not found on PATH — you may need to specify the full path.").dim()
        );
    }
    println!();

    let path: String = Input::new()
        .with_prompt("  Path to gemini-cli binary")
        .default("gemini".to_string())
        .interact_text()
        .context("Failed to read gemini path")?;

    println!("  {} Using: {path}", style("✔").green().bold());
    Ok(path)
}

fn ask_working_dir() -> Result<Option<String>> {
    step_header(4, 7, "Gemini Working Directory");
    println!(
        "  {}",
        style("Set a working directory for gemini-cli sessions.").dim()
    );
    println!(
        "  {}",
        style("Useful if you want Gemini to have access to a specific project.").dim()
    );
    println!();

    let use_dir = Confirm::new()
        .with_prompt("  Set a custom working directory?")
        .default(false)
        .interact()
        .context("Failed to read confirmation")?;

    if !use_dir {
        println!("  {} Using current directory.", style("✔").green().bold());
        return Ok(None);
    }

    let dir: String = Input::new()
        .with_prompt("  Working directory path")
        .validate_with(|input: &String| -> Result<(), String> {
            if Path::new(input).is_dir() {
                Ok(())
            } else {
                Err(format!("Directory '{}' does not exist", input))
            }
        })
        .interact_text()
        .context("Failed to read working directory")?;

    println!("  {} Using: {dir}", style("✔").green().bold());
    Ok(Some(dir))
}

fn ask_openai_key() -> Result<Option<String>> {
    step_header(5, 7, "Voice Transcription (OpenAI Whisper)");
    println!(
        "  {}",
        style("An OpenAI API key enables cloud voice transcription via OpenAI Whisper.").dim()
    );
    println!(
        "  {}",
        style("This is a fallback if local transcription is not enabled.").dim()
    );
    println!();

    let use_whisper = Confirm::new()
        .with_prompt("  Set up OpenAI Whisper API key?")
        .default(false)
        .interact()
        .context("Failed to read confirmation")?;

    if !use_whisper {
        println!("  {} OpenAI Whisper skipped.", style("✔").green().bold());
        return Ok(None);
    }

    let key: String = Password::new()
        .with_prompt("  OpenAI API key")
        .validate_with(|input: &String| -> Result<(), &str> {
            if input.starts_with("sk-") && input.len() > 10 {
                Ok(())
            } else {
                Err("Key should start with 'sk-' and be longer than 10 characters")
            }
        })
        .interact()
        .context("Failed to read API key")?;

    println!("  {} API key saved.", style("✔").green().bold());
    Ok(Some(key))
}

/// Returns (use_local_transcription, models_dir).
fn ask_local_transcription() -> Result<(bool, PathBuf)> {
    step_header(6, 7, "Local Transcription (Parakeet V3)");
    println!(
        "  {}",
        style("Parakeet V3 enables fully offline voice transcription.").dim()
    );
    println!(
        "  {}",
        style(format!(
            "Model size: ~{} MB (downloaded once).",
            transcription::MODEL_SIZE_MB
        ))
        .dim()
    );
    println!(
        "  {}",
        style("No API keys needed — everything runs locally.").dim()
    );
    println!();

    let enable = Confirm::new()
        .with_prompt("  Enable local transcription?")
        .default(true)
        .interact()
        .context("Failed to read confirmation")?;

    let default_dir = transcription::default_models_dir();

    if !enable {
        println!(
            "  {} Local transcription disabled.",
            style("✔").green().bold()
        );
        return Ok((false, default_dir));
    }

    let models_dir: String = Input::new()
        .with_prompt("  Models directory")
        .default(default_dir.display().to_string())
        .interact_text()
        .context("Failed to read models directory")?;
    let models_dir = PathBuf::from(&models_dir);

    // Download the model
    if transcription::is_model_downloaded(&models_dir) {
        println!("  {} Model already downloaded.", style("✔").green().bold());
    } else {
        let download_now = Confirm::new()
            .with_prompt("  Download Parakeet V3 model now?")
            .default(true)
            .interact()
            .context("Failed to read confirmation")?;

        if download_now {
            println!();
            println!("  {} Downloading Parakeet V3...", style("⬇").cyan().bold());

            // Run the async download using the existing tokio runtime.
            tokio::task::block_in_place(|| {
                tokio::runtime::Handle::current()
                    .block_on(transcription::download_model(&models_dir))
            })?;

            println!("  {} Model ready!", style("✔").green().bold());
        } else {
            println!(
                "  {} {}",
                style("⚠").yellow().bold(),
                style("Model will be downloaded on first bot start.").yellow()
            );
        }
    }

    Ok((true, models_dir))
}

fn ask_log_level() -> Result<String> {
    step_header(7, 7, "Log Level");

    let levels = vec!["error", "warn", "info", "debug", "trace"];
    let default_idx = 2; // "info"

    let selection = Select::new()
        .with_prompt("  Select log level")
        .items(&levels)
        .default(default_idx)
        .interact()
        .context("Failed to read log level")?;

    let level = levels[selection].to_string();
    println!("  {} Log level: {level}", style("✔").green().bold());
    Ok(level)
}

