fn preview_config(config: &SetupConfig) {
    println!();
    println!(
        "  {}",
        style("┌─────────────────────────────────────────────┐").cyan()
    );
    println!(
        "  {}",
        style("│           Configuration Summary             │").cyan()
    );
    println!(
        "  {}",
        style("└─────────────────────────────────────────────┘").cyan()
    );
    println!();

    let mask = |s: &str| -> String {
        if s.len() <= 8 {
            "••••••••".to_string()
        } else {
            format!("{}••••••••", &s[..4])
        }
    };

    println!(
        "  {:<24} {}",
        style("Bot Token:").bold(),
        style(mask(&config.telegram_bot_token)).dim()
    );
    println!(
        "  {:<24} {}",
        style("Allowed Users:").bold(),
        config
            .allowed_user_ids
            .as_deref()
            .unwrap_or("all (unrestricted)")
    );
    println!(
        "  {:<24} {}",
        style("Gemini CLI:").bold(),
        config.gemini_cli_path
    );
    println!(
        "  {:<24} {}",
        style("Working Dir:").bold(),
        config
            .gemini_working_dir
            .as_deref()
            .unwrap_or("(current directory)")
    );
    println!(
        "  {:<24} {}",
        style("OpenAI Key:").bold(),
        config
            .openai_api_key
            .as_ref()
            .map(|k| mask(k))
            .unwrap_or_else(|| "not set".to_string())
    );
    println!(
        "  {:<24} {}",
        style("Local Transcription:").bold(),
        if config.use_local_transcription {
            style("enabled (Parakeet V3)").green().to_string()
        } else {
            "disabled".to_string()
        }
    );
    if config.use_local_transcription {
        println!(
            "  {:<24} {}",
            style("Models Dir:").bold(),
            config.models_dir.display()
        );
    }
    println!("  {:<24} {}", style("Log Level:").bold(), config.rust_log);

    println!();
}

fn write_env_file(config: &SetupConfig) -> Result<()> {
    let env_path = Path::new(".env");

    if env_path.exists() {
        println!(
            "  {} {}",
            style("⚠").yellow().bold(),
            style("A .env file already exists!").yellow(),
        );

        let choices = vec!["Overwrite", "Back up & overwrite", "Cancel"];
        let selection = Select::new()
            .with_prompt("  What would you like to do?")
            .items(&choices)
            .default(1)
            .interact()
            .context("Failed to read selection")?;

        match selection {
            0 => { /* overwrite directly */ }
            1 => {
                let backup = ".env.backup";
                std::fs::copy(env_path, backup)
                    .with_context(|| format!("Failed to create backup at {backup}"))?;
                println!(
                    "  {} Backup saved to {}",
                    style("✔").green().bold(),
                    style(backup).underlined()
                );
            }
            _ => {
                println!("  {} Setup cancelled.", style("✘").red().bold());
                return Ok(());
            }
        }
    }

    let content = config.to_env_string();
    std::fs::write(env_path, &content).context("Failed to write .env file")?;

    println!();
    println!(
        "  {} {}",
        style("✔").green().bold(),
        style(".env file written successfully!").green().bold()
    );
    println!();
    println!("  {}", style("Start the bot with:").dim());
    println!("    {}", style("cargo run --release").cyan().bold());
    println!();

    Ok(())
}

