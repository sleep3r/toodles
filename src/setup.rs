/// Run the interactive setup wizard.
pub fn run_setup() -> Result<()> {
    println!("{}", style(BANNER).cyan().bold());
    println!(
        "  {}",
        style("Interactive setup wizard — let's configure your bot!").bold()
    );

    let telegram_bot_token = ask_telegram_token()?;
    let allowed_user_ids = ask_allowed_users()?;
    let gemini_cli_path = ask_gemini_cli_path()?;
    let gemini_working_dir = ask_working_dir()?;
    let openai_api_key = ask_openai_key()?;
    let (use_local_transcription, models_dir) = ask_local_transcription()?;
    let rust_log = ask_log_level()?;

    let config = SetupConfig {
        telegram_bot_token,
        allowed_user_ids,
        gemini_cli_path,
        gemini_working_dir,
        openai_api_key,
        use_local_transcription,
        models_dir,
        rust_log,
    };

    preview_config(&config);

    let confirmed = Confirm::new()
        .with_prompt("  Write this configuration to .env?")
        .default(true)
        .interact()
        .context("Failed to read confirmation")?;

    if confirmed {
        write_env_file(&config)?;
    } else {
        println!("  {} Setup cancelled.", style("✘").red().bold());
    }

    Ok(())
}
