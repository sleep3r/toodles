struct SetupConfig {
    telegram_bot_token: String,
    allowed_user_ids: Option<String>,
    gemini_cli_path: String,
    gemini_working_dir: Option<String>,
    openai_api_key: Option<String>,
    use_local_transcription: bool,
    models_dir: PathBuf,
    rust_log: String,
}

impl SetupConfig {
    /// Render as `.env` file content.
    fn to_env_string(&self) -> String {
        let mut lines = Vec::new();

        lines.push(
            "# ──────────────────────────────────────────────────────────────────────────────"
                .to_string(),
        );
        lines.push("# Toodles – generated by `toodles --setup`".to_string());
        lines.push(
            "# ──────────────────────────────────────────────────────────────────────────────"
                .to_string(),
        );
        lines.push(String::new());

        lines.push(format!("TELEGRAM_BOT_TOKEN={}", self.telegram_bot_token));
        lines.push(String::new());

        if let Some(ids) = &self.allowed_user_ids {
            lines.push(format!("ALLOWED_USER_IDS={ids}"));
        } else {
            lines.push("# ALLOWED_USER_IDS=".to_string());
        }
        lines.push(String::new());

        lines.push(format!("GEMINI_CLI_PATH={}", self.gemini_cli_path));
        lines.push(String::new());

        if let Some(dir) = &self.gemini_working_dir {
            lines.push(format!("GEMINI_WORKING_DIR={dir}"));
        } else {
            lines.push("# GEMINI_WORKING_DIR=".to_string());
        }
        lines.push(String::new());

        if let Some(key) = &self.openai_api_key {
            lines.push(format!("OPENAI_API_KEY={key}"));
        } else {
            lines.push("# OPENAI_API_KEY=".to_string());
        }
        lines.push(String::new());

        if self.use_local_transcription {
            lines.push("USE_LOCAL_TRANSCRIPTION=true".to_string());
            lines.push(format!("MODELS_DIR={}", self.models_dir.display()));
        } else {
            lines.push("# USE_LOCAL_TRANSCRIPTION=false".to_string());
            lines.push(format!("# MODELS_DIR={}", self.models_dir.display()));
        }
        lines.push(String::new());

        lines.push(format!("RUST_LOG={}", self.rust_log));
        lines.push(String::new());

        lines.join("\n")
    }
}

